package br.pucrs.g.girelli.exemplo;

public class Livro {
    public int id;
    public String titulo;
    public String autor;
    public int ano;

    public Livro(int id, String titulo, String autor, int ano) {
        this.id = id;
        this.titulo = titulo;
        this.autor = autor;
        this.ano = ano;
    }
    
}
